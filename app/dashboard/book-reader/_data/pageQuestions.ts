// Get questions for a specific page
export function getPageQuestions(chapterId: string, page: number): {
  questions: PageQuestion[];
  suggestions: string[];
} {
  // For now, use chapter 1 questions. Can be extended for other chapters
  if (chapterId === 'ch1' || chapterId === 'chapter-1') {
    return chapter1Questions[page] || {
      questions: [],
      suggestions: [
        'এই পৃষ্ঠায় কী আলোচনা করা হয়েছে?',
        'এই বিষয়টি সম্পর্কে আরো জানতে চাই',
        'এটি কীভাবে কাজ করে?',
      ],
    };
  }

  // Default questions for other chapters
  return {
    questions: [],
    suggestions: [
      'এই পৃষ্ঠায় কী আলোচনা করা হয়েছে?',
      'এই বিষয়টি সম্পর্কে আরো জানতে চাই',
      'এটি কীভাবে কাজ করে?',
    ],
  };
}

// Get a random suggestion question for a page
export function getRandomSuggestion(chapterId: string, page: number): string {
  const pageData = getPageQuestions(chapterId, page);
  if (pageData.suggestions.length > 0) {
    return pageData.suggestions[Math.floor(Math.random() * pageData.suggestions.length)];
  }
  return 'এই পৃষ্ঠায় কী আলোচনা করা হয়েছে?';
}

